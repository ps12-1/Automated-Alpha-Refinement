class node:
    def __init__(self, data):
      self.data = data
      self.left = self.right = None
      self.fixed = None
      self.flag = 0

    def insert(self, left, right):
      if left == "" :
        self.left = None
      else:
        self.left = node(left)
      if right == "" :
        self.right = None
      else:
        self.right = node(right)

    def set_flag(self, fixed):
      self.fixed = fixed
      self.flag=1


    def PrintTree(self):
      if self.left:
         self.left.PrintTree()
      print( self.data),
      if self.right:
         self.right.PrintTree()

    def cloning(self, n) :
      if (n != None) :
        point = node(n.data)
        point.flag=n.flag
        point.fixed=n.fixed
        point.left = self.cloning(n.left)
        point.right = self.cloning(n.right)
        return point

      return None

    def cloneTree(self) :
      tree = self.cloning(self)
      return tree


