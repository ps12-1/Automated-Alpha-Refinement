def dfs(root):
    if root:
      count = 1 + dfs(root.left) + dfs(root.right)
      return count
    else:
      return 0

def bfs(root,position):
    queue = []
    queue.append(root)
    cnt=0
    while(len(queue) > 0):
      node = queue.pop(0)
      cnt+=1
      if cnt== position:
        return node
      if node.left is not None:
            queue.append(node.left)

      if node.right is not None:
          queue.append(node.right)
    return None

